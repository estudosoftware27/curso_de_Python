# Strings
# O que é uma string?
# Uma string é uma série de caracteres.
# Tudo que estiver entre aspas é considerada uma string em Python.
# Você pode usar aspas simples --> ' '
# ou você pode usar aspas duplas --> "  "

# Usando aspas duplas --> "Só sei que nada sei."
# usando aspas simples --> 'Só sei que nada sei.'

# Essa flexibilidade permite usar aspas e apóstrofos em suas strings:
mensagem = "Ela disse: 'O projeto está pronto', e então sorriu."
print(mensagem)

# Saída de impressão:
# Ela disse: 'O projeto está pronto', e então sorriu.

mensagem = 'Ele respondeu: "Vamos começar agora mesmo!"'
print(mensagem)

# Saída de impressão:
# Ele respondeu: "Vamos começar agora mesmo!"
