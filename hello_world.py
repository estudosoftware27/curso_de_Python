print("Hello Python world!")

# Quando executar esse código, você deverá ver a saída a seguir:

# Hello Python World!

# Ao executar hello_world.py, o arquivo terminado em .py indica que é
# um programa python.

# Quando o interpretador vê a palavra print, ele exibe na tela o que
# quer que esteja dentro dos parênteses.

mensagem = "Hello Python World!"
print(mensagem)

# Executando esse programa vemos a mesma saída visto anteriormente:
# Hello Python World!

# Foi acrescentado uma váriavel chamada mensagem.
# Toda variável armazena um valor.
# O valor é a informação associada a essa variável.

# mensagem --> é a variável
# Hello Python World! --> é a informação associada a essa variável.

# O python sempre manterá o controle do valor atual.
# Podemos mudar o valor de uma variável em nosso programa a qualquer
# momento.

# Ao executar hello_world.py aparecerá duas linhas na saída:

mensagem = "Eu adoro estudar Python!"
print(mensagem)